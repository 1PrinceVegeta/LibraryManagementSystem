    private void displaybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displaybtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel(new String[]{"User ID", "Last Name", "First Name","Loan Duration"}, 0);
        
        try {
            Registry r=LocateRegistry.getRegistry("127.0.0.1", 1099);
            BookManager bM=(BookManager) r.lookup("LibrarySystemService");
            ArrayList<User> userList=bM.getUsers();
            for(int i=0; i<userList.size(); i++){
                model.addRow(new Object[]{userList.get(i).getUserId(), userList.get(i).getUserFirstName(), userList.get(i).getUserLastName(), userList.get(i).getUserLoanDuration()});
            }
            jTable1.setModel(model);
        }catch(RemoteException | NotBoundException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_displaybtnActionPerformed

