    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        try{
            int uid=Integer.parseInt(useridtxt.getText());
            int duration=Integer.parseInt(durationtxt.getText());            
            Registry r=LocateRegistry.getRegistry("127.0.0.1", 1099);
            BookManager bM= (BookManager) r.lookup("LibrarySystemService");
            bM.updateLoanDuration(duration, uid);                                    
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_updatebtnActionPerformed

